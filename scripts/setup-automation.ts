#!/usr/bin/env bun
/**
 * Setup Automation
 *
 * One-time setup to make testing fully hands-free.
 * Run: bun run scripts/setup-automation.ts
 */

import { existsSync, mkdirSync, writeFileSync, chmodSync } from 'fs';
import { join } from 'path';
import { execSync } from 'child_process';

const PROJECT_ROOT = join(import.meta.dir, '..');
const GIT_ROOT = join(PROJECT_ROOT, '..', '..', '..');
const HOOKS_DIR = join(GIT_ROOT, '.git', 'hooks');

console.log('Setting up hands-free test automation...\n');

// 1. Install git hooks
console.log('1. Installing git hooks...');
if (existsSync(HOOKS_DIR)) {
  const prePushHook = `#!/bin/sh
# Auto-generated by voice_ai_agents setup
cd workflows/voice_ai_agents 2>/dev/null || exit 0
echo "Running tests before push..."
bun run test:run --reporter=dot
if [ $? -ne 0 ]; then
  echo "\\n❌ Tests failed. Push aborted."
  echo "Run 'bun run test' to see details."
  exit 1
fi
echo "✓ All tests passed"
`;

  const hookPath = join(HOOKS_DIR, 'pre-push');
  writeFileSync(hookPath, prePushHook);
  try {
    chmodSync(hookPath, '755');
  } catch {
    // Windows doesn't need chmod
  }
  console.log('   ✓ pre-push hook installed');
} else {
  console.log('   ⚠ Git hooks directory not found - skipping');
}

// 2. Verify dependencies
console.log('\n2. Verifying dependencies...');
try {
  execSync('bun --version', { stdio: 'pipe' });
  console.log('   ✓ Bun is installed');
} catch {
  console.log('   ❌ Bun is not installed. Install from: https://bun.sh');
  process.exit(1);
}

// 3. Install npm dependencies
console.log('\n3. Installing dependencies...');
try {
  execSync('bun install', { cwd: PROJECT_ROOT, stdio: 'inherit' });
  console.log('   ✓ Dependencies installed');
} catch {
  console.log('   ❌ Failed to install dependencies');
}

// 4. Verify test suite
console.log('\n4. Verifying test suite...');
try {
  execSync('bun run test:run --reporter=dot', { cwd: PROJECT_ROOT, stdio: 'pipe' });
  console.log('   ✓ Test suite passes');
} catch {
  console.log('   ⚠ Some tests are failing (this is OK for setup)');
}

// 5. Create .test-data directory
console.log('\n5. Setting up test data directory...');
const testDataDir = join(PROJECT_ROOT, '.test-data');
if (!existsSync(testDataDir)) {
  mkdirSync(testDataDir, { recursive: true });
}
console.log('   ✓ .test-data directory ready');

console.log('\n' + '═'.repeat(50));
console.log('\n✅ Automation setup complete!\n');
console.log('What happens now (hands-free):');
console.log('  • Git push → Tests run automatically → Blocks if failing');
console.log('  • GitHub Actions → Tests on every PR');
console.log('  • Every 6 hours → Health check runs (if n8n workflow deployed)');
console.log('\nYou will only hear from this system if something breaks.\n');
