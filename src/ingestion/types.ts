/**
 * @wranngle/voice-evals/ingestion — types for the LLM data-translation layer.
 *
 * Inputs to ingestion are usually conversational artifacts: ElevenLabs
 * post-call webhook payloads, plain-text transcripts, or chat logs from
 * other voice platforms. Outputs are structured TestCase + assertion specs
 * that the eval engine can score.
 */

import type {TestCase} from '../testing/types';

/**
 * Minimal shape of an ElevenLabs post-call webhook payload (`post_call_transcription`).
 * The full SDK type lives at `@elevenlabs/elevenlabs-js`; we mirror only the
 * fields ingestion reads so callers can pass plain JSON without a SDK type.
 */
export type ElevenLabsPostCallPayload = {
  type: 'post_call_transcription' | 'post_call_audio' | 'call_initiation_failure' | string;
  event_timestamp?: number | string;
  data?: {
    agent_id?: string;
    conversation_id?: string;
    transcript?: Array<{
      role: 'agent' | 'user' | 'tool' | string;
      message?: string;
      tool_calls?: unknown[];
      tool_results?: unknown[];
      time_in_call_secs?: number;
      conversation_turn_metrics?: Record<string, unknown>;
    }>;
    metadata?: {
      start_time?: string | number;
      duration?: number;
      cost?: number;
      termination_reason?: string;
    };
    analysis?: {
      evaluation_criteria_results?: Record<string, {
        criteria_id?: string;
        result?: 'success' | 'failure' | 'unknown' | string;
        rationale?: string;
      }>;
      data_collection_results?: Record<string, unknown>;
      call_successful?: 'success' | 'failure' | 'unknown' | string;
      transcript_summary?: string;
    };
    conversation_initiation_client_data?: Record<string, unknown>;
  };
};

/** A proposed test case generated by the LLM data layer, awaiting human review. */
export type ProposedTestCase = {
  /** Suggested test_id (kebab-case). May be overwritten by the caller. */
  suggested_id: string;
  name: string;
  description: string;
  /** What the agent should DO. Used by the human reviewer to triage. */
  intent: string;
  /** Suggested simulated user input. */
  simulated_user: {
    first_message: string;
    profile?: string;
  };
  /** Free-text assertions for the human to convert into structured form. */
  draft_assertions: string[];
  /** Optional persona slug; see persona-generator. */
  persona?: string;
};

/**
 * LLM completion callback — accept a prompt, return raw text.
 *
 * We intentionally do NOT bind to Anthropic / OpenAI / Google SDKs so consumers
 * plug their preferred provider with this same shape. Default suggested impl:
 * a one-shot completion with temperature 0 for determinism.
 */
export type LlmCompleteCallback = (options: {
  system: string;
  user: string;
  /** Optional response-format hint. `'json'` asks the callback to coerce to JSON. */
  responseFormat?: 'text' | 'json';
}) => Promise<string>;

/** Options shared across LLM-driven ingestion calls. */
export type IngestionOptions = {
  /** LLM callback for the proposer + designer. Required. */
  llm: LlmCompleteCallback;
  /** Optional tag(s) to apply to every produced TestCase. */
  tags?: string[];
  /** Optional id prefix for produced TestCases. Default: 'TC-INGEST'. */
  idPrefix?: string;
};

/** Output of the post-call deterministic importer. */
export type ImportedTestCases = {
  cases: TestCase[];
  source: {
    agent_id?: string;
    conversation_id?: string;
    transcript_summary?: string;
  };
};
